# 冒泡排序,比较相邻的两个元素
a = [150, 140, 170, 140, 157, 158]
#   [140,150,170,140,157,158]
#   [140,150,170,140,157,158]
#   [140,150,140,170,157,158]
#   [140,150,140,157,170,158]
#   [140,150,140,157,158,170]
c = 0
n = len(a)
for j in range(n-1):
    for i in range(n-1-j):
        c += 1
        if a[i] > a[i + 1]:
            a[i], a[i + 1] = a[i + 1], a[i]
print(a,"比较了%d次"%c)

